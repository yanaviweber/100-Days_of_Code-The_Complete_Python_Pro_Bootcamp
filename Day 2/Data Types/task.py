# Subscripting

# print last symbol
print("Hello"[4]) # Output o
print("Hello"[-1]) # Output o

# Sprint symbol, which is in 0 position
print("Hello"[0]) # Output H

# Sprint symbol, which is in 01position
print("Hello"[1]) # Output e
# String
print("123" + "345") # Concatenation 2 strings

# Integer = whole number
print(123 + 345) #Output sum 468

# Large Integer
print(123_456_789) # Output 123456789

# Float = Floating Point Number
print(3.14159) #Output 3.14159

# Boolean
print(True) #Output True
print(False) #Output False
