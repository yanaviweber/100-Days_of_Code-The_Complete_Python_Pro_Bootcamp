# Subcripting

# print last symbol
print("Hello"[4])
print("Hello"[-1])

# Sprint symbol, which is in 0 position
print("Hello"[0])

# Sprint symbol, which is in 01position
print("Hello"[1])

# String
print("123" + "345") # Concatenation 2 strings

# Integer = whole number
print(123 + 345)

# Large Integer
print(123_456_789)

# Float = Floating Point Number
print(3.14159)

# Boolean
print(True)
print(False)
